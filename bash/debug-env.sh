#!/bin/bash

# 获取当前工作目录下的日志文件路径
# 注意：因为通常用 sudo 运行 openconnect，为了避免权限问题导致找不到文件
# 我们显式获取当前脚本所在的目录
LOG_FILE="$HOME/vpn_env_dump.log"

# 开始记录
echo "=======================================================" >> "$LOG_FILE"
echo "触发时间: $(date)" >> "$LOG_FILE"
echo "调用原因 (reason): ${reason}" >> "$LOG_FILE"
echo "-------------------------------------------------------" >> "$LOG_FILE"

# 打印所有环境变量
# env 命令列出所有变量
# sort 排序方便查看
# grep -v 过滤掉一些无关紧要的系统变量（可选，为了纯净这里不过滤）
env | sort >> "$LOG_FILE"

echo "=======================================================" >> "$LOG_FILE"
echo "" >> "$LOG_FILE"

# 这是一个调试脚本，不做任何网络配置
# 告诉 OpenConnect 我们执行完了
exit 0